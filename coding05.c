#include <stdio.h>

int main() {
    int A, B, C, D, E, F, G;

    // A = -2 + 5 * 2;
    A = -2 + 5 * 2; // 5*2=10 => -2+10=8

    // B = 10 / 2 * 3;
    B = 10 / 2 * 3; // 10/2=5 => 5*3=15

    // C = 6 / 2 + 3 * (4 % 2);
    C = 6 / 2 + 3 * (4 % 2); // 6/2=3, 4%2=0 => 3 + 3*0 = 3

    // D = (5 + 2) * 15 % 4;
    D = (5 + 2) * 15 % 4; // (7)*15=105, 105%4=1

    // E = 6 + 2 * 2 - 6 / 2;
    E = 6 + 2 * 2 - 6 / 2; // 2*2=4, 6/2=3 => 6+4-3=7

    // F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    F = 5 + 3 * 2 - 8 / 4 + (6 % 5); // 3*2=6, 8/4=2, 6%5=1 => 5+6-2+1=10

    // G = (4 + 3) * 2 - 10 / (2 + 3);
    G = (4 + 3) * 2 - 10 / (2 + 3); // (7)*2=14, 10/5=2 => 14 - 2 =12

    // แสดงผลลัพธ์
    printf("A = %d\n", A);
    printf("B = %d\n", B);
    printf("C = %d\n", C);
    printf("D = %d\n", D);
    printf("E = %d\n", E);
    printf("F = %d\n", F);
    printf("G = %d\n", G);

    return 0;
}